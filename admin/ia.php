<div class="container" id="oq">
      <div class="col-md-12 mx-auto text-right" id="oq">
<h6 class="fw-bold display-6 text-purple"></h6>
</div>
<div class="col-md-12 col-lg-12 col-sm-6 mx-auto text-align">
<p class="landing-content-desc mt-3" style="text-align: justify"></p>
<p class="landing-content-desc mt-3" style="text-align: justify">Aqui estão alguns conceitos-chave relacionados à inteligência artificial:</p>
<p class="landing-content-desc mt-3" style="text-align: justify"><span class="text-purple">Aprendizado de Máquina (Machine Learning):</span>  É uma subárea da IA que se concentra em criar algoritmos que podem aprender a partir de dados. Os modelos de aprendizado de máquina são treinados usando conjuntos de dados para fazer previsões ou tomar decisões sem programação explícita.</p>
                     <p class="landing-content-desc mt-3" style="text-align: justify"><span class="text-purple">Redes Neurais Artificiais:</span> São modelos de IA inspirados na estrutura e funcionamento do cérebro humano. Essas redes são usadas em muitos aplicativos de aprendizado de máquina, como reconhecimento de padrões, processamento de linguagem natural e visão computacional.</p>
                     <p class="landing-content-desc mt-3" style="text-align: justify"><span class="text-purple">Processamento de Linguagem Natural (NLP):</span>É uma área da IA que se concentra na interação entre máquinas e linguagem humana. Os sistemas de NLP permitem que as máquinas compreendam, interpretem e gerem texto ou fala.
</p>   
                     <p class="landing-content-desc mt-3" style="text-align: justify"><span class="text-purple">Agentes Inteligentes: </span>São sistemas de IA que podem perceber o ambiente, tomar decisões com base em informações disponíveis e agir para atingir metas específicas. Chatbots e assistentes virtuais são exemplos de agentes inteligentes.</p>
                     <p class="landing-content-desc mt-3" style="text-align: justify"><span class="text-purple">Aprendizado Profundo (Deep Learning):</span> É uma técnica de aprendizado de máquina que envolve redes neurais profundas com várias camadas. O aprendizado profundo tem sido fundamental para avanços em tarefas de visão computacional, processamento de linguagem natural e muito mais.</p>
                     <p class="landing-content-desc mt-3" style="text-align: justify"><span class="text-purple">Raciocínio e Tomada de Decisão:</span>A IA também se preocupa com a capacidade de sistemas computacionais de raciocinar logicamente, resolver problemas complexos e tomar decisões com base em dados e regras pré-definidas. 
</p>
                     <p class="landing-content-desc mt-3" style="text-align: justify"><span class="text-purple">Robótica:</span> A IA é usada em robôs para permitir que eles naveguem em ambientes, interajam com objetos e até mesmo realizem tarefas complexas, como cirurgia assistida por robô.
</p>
                     <p class="landing-content-desc mt-3" style="text-align: justify"><span class="text-purple">Ética e Responsabilidade:</span>Com o avanço da IA, surgem questões éticas e de responsabilidade, como privacidade, viés algorítmico, segurança cibernética e a responsabilidade de sistemas autônomos.</p>
                     <p class="landing-content-desc mt-3" style="text-align: justify"><span class="text-purple">Aplicações Práticas:</span>A IA tem uma ampla gama de aplicações, incluindo assistentes virtuais, carros autônomos, diagnóstico médico, previsão do tempo, recomendações de filmes e muito mais.</p>
                     <p class="landing-content-desc mt-3" style="text-align: justify">A IA tem um potencial significativo para transformar muitos setores, melhorar a eficiência, automatizar tarefas repetitivas e ajudar a resolver problemas complexos. No entanto, também levanta questões importantes relacionadas à ética, segurança e impacto social, que precisam ser cuidadosamente consideradas à medida que a tecnologia continua a evoluir.</p>
                     
