<h6 class="fw-bold display-6 text-purple">O que é a inteligencia artificial?</h6>
</div>
<div class="col-md-12 col-lg-12 col-sm-6 mx-auto text-align">
<p class="landing-content-desc mt-3" style="text-align: justify">O termo Inteligência Artificial foi utilizado há muitas décadas atrás, mas ganhou uma enorme popularidade nos últimos anos graças ao aumento excessivo do volume de dados, algoritmos avançados e desenvolvimento de novas tecnologias de computação e armazenamento.</p>
<p class="landing-content-desc mt-3" style="text-align: justify">Por isso, a definição de IA pode ser bem óbvia no seu entendimento, mas vale a pena relembrarmos. </p>
<p class="landing-content-desc mt-3" style="text-align: justify">Em síntese, IA é um conjunto de sistemas diferentes que operam juntos para possibilitar que uma máquina seja capaz de executar tarefas, pensar, aprender e agir em um nível de inteligência semelhante aos seres humanos. </p>
<p class="landing-content-desc mt-3" style="text-align: justify">Os mecanismos mais conhecidos da IA são o machine learning e a PNL (programação neurolinguística). 

Quando aplicados juntamente a dados, análises e automação, fazem com que as máquinas aprendam e tomem decisões sem a ajuda de uma pessoa. </p>
